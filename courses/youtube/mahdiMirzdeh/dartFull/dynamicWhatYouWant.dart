/**
 * DYNAMIC, PUT EVERYTHING I WANT!
 */

void main() {
  // Dart does have dynamic types just like javascript, but they aren't recommended to use
  // so just like when I explicitly put a type like int, double, or String
  // I can use 'dynamic'
  dynamic age = 20;
  print(age/3);
  age = 'hello';
  // print(age/3); Error: Unhandled Exception: Class 'String' has no instance method '/'...

  
}
